              الفرامل الأمامية، وإذا ما كان ينبغي تبديل المورّدين أو التفاوض للحصول على أسعار أفضل.
            </p>
            <p>
              ويتمثل مبدأ ERP الأساسي في الجمع المركزي للبيانات للتوزيع على نطاق واسع. وبدلاً من
              وجود العديد من قواعد البيانات المستقلة مع مستودع لا نهائي يضم أوراق بيانات غير متصلة،
              تقوم أنظمة تخطيط موارد المؤسسات (ERP) بتنظيم هذه الفوضى كي يتمكن جميع المستخدمين بدءًا
              من المدير التنفيذي وانتهاءً بموظفي إدارة الحسابات مستحقة الدفع من إنشاء البيانات نفسها
              المشتقة خلال العمليات المشتركة وتخزينها واستخدامها. باستخدام أحد مستودعات البيانات
