          <section class="mb-5">
            <h2>بيان موجز عن تاريخ تخطيط موارد المؤسسة</h2>
            <p>
              <strong>من البطاقات الورقية إلى الأجهزة المحمولة‬‏‫</strong><br />
              ‏‫يعود<a
                class="magnify"
                data-cursor-magnify-sm
                href="https://en.wikipedia.org/wiki/Enterprise_resource_planning"
                target="_blank"
              >
                تاريخ ERP إلى أكثر من 100 عامًا.</a
              >
              ففي 1913، قام المهندس Ford Whitman Harris بتطوير ما صار يُعرف بنموذج "كمية الترتيب
              الاقتصادي" (EOQ)، وهو نظام صناعي يعتمد على الأوراق لجدولة الإنتاج. وقد استمرت EOQ طيلة
              عقود ترمز إلى الصناعة. قام Toolmaker Black وDecker بتغيير اللعبة في عام 1964 عندما
              كانت الشركة الأولى في تبني متطلبات المواد التي تخطط لحل (MRP) الذي ضم مفاهيم EOQ
              باستخدام جهاز كمبيوتر بإطار رئيسي.
            </p>
            <p>
              وقد بقي MRP (حل تخطيط متطلبات المواد) معيار التصنيع إلى أن تم تطوير حل تخطيط موارد
              التصنيع (يُطلق عليه MRP II) في عام 1983. وقد تميز حل تخطيط موارد التصنيع (MRP II)
              "بالوحدات" باعتبارها مكونًا هندسيًا برمجيًا أساسيًا ودمج مكونات التصنيع الرئيسية التي
              تشمل الشراء وفوترة المواد والجدولة وإدارة العقود. تم تكامل مختلف المهام الصناعية لأول
              مرة في نظام مشترك. قدمت MRP II رؤية ساحرة حول كيفية استفادة المؤسسات من البرنامج في
              مشاركة بيانات المؤسسة ودمجها وتفعيل الكفاءة التشغيلية مع تخطيط إنتاجي أفضل، وانخفاض
